"""utils"""


def add(a: int, b: int) -> int:
    """add"""
    return a + b


def subtract(a: int, b: int) -> int:
    """subtract"""
    return a - b


def multiply(a: int, b: int) -> int:
    """multiply"""
    return a * b


def divide(a: int, b: int) -> float:
    """divide"""
    return a / b


def power(a: int, b: int) -> int:
    """power"""
    return a**b
